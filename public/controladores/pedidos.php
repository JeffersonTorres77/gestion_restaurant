class Controlador extends ControladorBase
{
    /*============================================================================
	 *
	 *	Constructor
	 *
    ============================================================================*/
    public function __construct()
    {
        try {
            $this->ValidarSesion();
        } catch(Exception $e) {
            if(!Sesion::Validar()) {
                throw new Exception("No se ha iniciado sesiÃ³n");
            }
        }

        if( !Peticion::getEsAjax() )
        {
            Incluir::Template("modelo_cliente");
            Template::Iniciar();
        }
    }
    
    /*============================================================================
	 *
	 *	Destructor
	 *
    ============================================================================*/
    public function __destruct()
    {
        if(!Peticion::getEsAjax()) {
            Template::Finalizar();
        }
    }
    
    /*============================================================================
	 *
	 *	
	 *
    ============================================================================*/
    public function carrito()
    {
        $this->AJAX("pedidos/carrito");
    }
}